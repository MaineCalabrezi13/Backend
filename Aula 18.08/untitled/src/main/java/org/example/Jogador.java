package org.example;


public class Jogador {
    String nome;
    String sobrenome;
    String posicao;
    String time;
    int idade;

    public Jogador(String nome, String sobrenome, String posicao, String time, int idade) {
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.posicao = posicao;
        this.time = time;
        this.idade = idade;
    }
}